func TestBuildScopedReaderGrantSQLRejectsUnsafeIdentifiers(t *testing.T) {
	allowlist := []string{"get_sync_status", "summarize_call_facts", "summarize_calls_by_lifecycle", "rank_transcript_backlog"}
	for _, tc := range []struct {
		name     string
		roleName string
		dbName   string
	}{
		{name: "role injection", roleName: `reader"; DROP ROLE writer; --`, dbName: "gongctl"},
		{name: "database dash", roleName: "gongmcp_reader", dbName: "gongctl-prod"},
		{name: "role starts digit", roleName: "1_reader", dbName: "gongctl"},
	} {
		t.Run(tc.name, func(t *testing.T) {
			_, err := BuildScopedReaderGrantSQL(ScopedReaderGrantSQLParams{
				Allowlist:    allowlist,
				RoleName:     tc.roleName,
				DatabaseName: tc.dbName,
			})
			if err == nil {
				t.Fatal("BuildScopedReaderGrantSQL accepted unsafe identifier")
			}
		})
	}
}
